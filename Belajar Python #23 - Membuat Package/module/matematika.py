def penjumlahan(value1 = 1, value2 = 1):
 jumlah = value1 + value2
 return jumlah

def pengurangan(value1 = 1, value2 = 1):
 jumlah = value1 - value2
 return jumlah

def pembagian(value1 = 1, value2 = 1):
 jumlah = value1 / value2
 return jumlah

def perkalian(value1 = 1, value2 = 1):
 jumlah = value1 * value2
 return jumlah